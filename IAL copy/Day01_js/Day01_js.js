function setup() {
 //create a canvas that is 800 px by 600 px
 createCanvas(800, 600);
 //gives color a background
 background(200, 150, 100);

}


function draw() {
  
 //gives color a background
 background(200, 150, 100);
 
 
 // gives stroke reddish color
 stroke(200, 50, 50);
 
 //draw a line from point to point
 line(100, 100, 250, 250)
 
 // set stroke to blue
 stroke(50, 50, 200); 

 
 // set the fill to  black
 fill(50); 
 
 // create rectangle starting a a point and with a width and height
 rect(300, 200, 200, 300,);
 
 // resets stroke and fill to new colors
 stroke(50, 200, 50);
 fill (200);
 ellipse(500, 500, 100, 100);
 

}
